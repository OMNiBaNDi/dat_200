def bubble_sort_recursive(arr, n):
    #base case
    if n <= 1:
        return
    for i in range(len(arr) - 1):
        if arr[i] > arr[i + 1]:
            arr[i], arr[i + 1] = arr[i + 1], arr[i]
    bubble_sort_recursive(arr, n - 1)


